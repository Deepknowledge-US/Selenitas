function love.conf(t)
    t.version = "11.3"

    -- Window settings
    t.window.title = "Selenitas"
    t.window.resizable = true
    t.window.width = 0 -- 0 means the user's desktop size
    t.window.height = 0
    t.window.minwidth = 10
    t.window.minheight = 10
    
    --t.console = true
end
